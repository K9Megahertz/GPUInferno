#include "reshapebackward.h"
#include "inferno/gradengine/engine.h"

namespace Inferno {


	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	//
	//  CTORS / DTORS
	//
	//
	//
	//
	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

	ReshapeBackward::ReshapeBackward(const Tensor& A) : m_A(A) {
		set_name("ReshapeBackward");

	}


	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	//
	//  Function backward
	//
	//
	//
	//
	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

	void ReshapeBackward::backward() {
		NoGradGuard guard;

		Tensor g = Engine::grad_in(this, 0);

		if (!g.is_contiguous()) {
			g = g.contiguous();
		}

		Tensor g_a = g.reshape(m_A.shape());

		auto na = GetImpl(m_A)->grad_edge();
		if (na) {
			Engine::accumulate(na.get(), 0, g_a);
		}
	}


	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	//
	//  Function get_inputs
	//
	//
	//
	//
	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

	void ReshapeBackward::get_inputs(std::vector<Tensor>& out) const {
		out.push_back(m_A);
	}


	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	//
	//  Function release
	//
	//
	//
	//
	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

	void ReshapeBackward::release() {
		m_A = Tensor{};
	}

}