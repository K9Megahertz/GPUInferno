    CUDAStorage::CUDAStorage(size_t numbytes) {
        
		check_cuda(cudaMalloc(&ptr, numbytes), "CUDAstorage failed to cudaMalloc");
        m_numbytes = numbytes;
    }

    CUDAStorage::~CUDAStorage() {        
		check_cuda(cudaFree(ptr), "CUDAstorage failed to cudaFree");
    }
