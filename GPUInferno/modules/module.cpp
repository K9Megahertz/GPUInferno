#include "module.h"

namespace Inferno {


	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	//
	//  Function << overload
	//
	//
	//
	//
	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

	std::ostream& operator<<(std::ostream& os, const Module& module) {

		for (auto mod : module.parameters()) {
			os << *mod << std::endl;
		}

		return os;
	}

}